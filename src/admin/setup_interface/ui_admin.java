    private String[] get_data_attribute(String mulai) {
        String id_admin = admin_kode, hostname = null, status = "0";
        if ("Mulai".equals(mulai)) {
            try {
                ip = InetAddress.getLocalHost();
                hostname = "'" + ip.getHostAddress() + "'";
                status = String.valueOf(1);
            } catch (UnknownHostException ex) {
                Logger.getLogger(ui_admin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String data[] = new String[]{id_admin, hostname, status};
        return data;
    }

