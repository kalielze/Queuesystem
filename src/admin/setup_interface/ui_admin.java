    @SuppressWarnings("OverridableMethodCallInConstructor")
    public ui_admin(String admin_kode, Component com) {
        try {
            initComponents();
            setLocationRelativeTo(null);
            setResizable(false);
            ImageIcon ico = new ImageIcon((getClass().getResource("/image/IconDisplay.png")));
            setIconImage(ico.getImage());
            nomor.requestFocus();
            this.admin_kode = admin_kode;
            sa.connection(con.connection());
            ua.connection(con.connection());
            boolean bl = sa.check_admin_status(Integer.parseInt(admin_kode), com);
            if (bl == false) {
                sa.get_client_list_admin(antrian_biasa);
                sa.get_client_list_cashier(antrian_kasir);
                ip = InetAddress.getLocalHost();
                ua.set_admin(get_data_attribute("Mulai"));
                admin adm = new admin(antrian_biasa, antrian_kasir);
                ar.registry(ip, Integer.parseInt(admin_kode), adm);
            } else {
                System.exit(0);
            }
        } catch (UnknownHostException ex) {
            Logger.getLogger(ui_admin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(ui_admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

